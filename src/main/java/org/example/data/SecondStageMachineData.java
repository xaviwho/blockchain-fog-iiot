package org.example.data;

/**
 * Data for a second stage machine (Machines 4-5)
 */
public class SecondStageMachineData {
    private float temperature1;
    private float temperature2;
    private float temperature3;
    private float temperature4;
    private float temperature5;
    private float temperature6;
    private float pressure;
    private float exitTemperature;

    // Getters and setters
    public float getTemperature1() { return temperature1; }
    public void setTemperature1(float temperature1) { this.temperature1 = temperature1; }

    public float getTemperature2() { return temperature2; }
    public void setTemperature2(float temperature2) { this.temperature2 = temperature2; }

    public float getTemperature3() { return temperature3; }
    public void setTemperature3(float temperature3) { this.temperature3 = temperature3; }

    public float getTemperature4() { return temperature4; }
    public void setTemperature4(float temperature4) { this.temperature4 = temperature4; }

    public float getTemperature5() { return temperature5; }
    public void setTemperature5(float temperature5) { this.temperature5 = temperature5; }

    public float getTemperature6() { return temperature6; }
    public void setTemperature6(float temperature6) { this.temperature6 = temperature6; }

    public float getPressure() { return pressure; }
    public void setPressure(float pressure) { this.pressure = pressure; }

    public float getExitTemperature() { return exitTemperature; }
    public void setExitTemperature(float exitTemperature) { this.exitTemperature = exitTemperature; }
}